    /// Unknown or unhandled key.
    /// </summary>
    Unknown = 0,

    /// <summary>
    /// The Enter/Return key.
    /// </summary>
    Enter,

    /// <summary>
    /// The Escape key.
    /// </summary>
    Escape,

    /// <summary>
    /// The Space bar.
    /// </summary>
    Space,

    /// <summary>
    /// The left Control key.
    /// </summary>
    LeftCtrl,

    /// <summary>
    /// The right Control key.
    /// </summary>
    RightCtrl,

    /// <summary>
    /// The left Shift key.
    /// </summary>
    LeftShift,

    /// <summary>
    /// The right Shift key.
    /// </summary>
    RightShift,

    /// <summary>
    /// The left Alt key.
    /// </summary>
    LeftAlt,

    /// <summary>
    /// The right Alt key.
    /// </summary>
    RightAlt,

    /// <summary>
    /// A generic key slot for letter keys.
    /// </summary>
    A,

    /// <summary>
    /// A generic key slot for letter keys.
    /// </summary>
    B,

    /// <summary>
    /// A generic key slot for letter keys.
    /// </summary>
    C,

    /// <summary>The Tab key.</summary>
    Tab,
    /// <summary>The Backspace key.</summary>
    Backspace,
    /// <summary>The Left arrow key.</summary>
    Left,
    /// <summary>The Right arrow key.</summary>
    Right,
    /// <summary>The Up arrow key.</summary>
    Up,
    /// <summary>The Down arrow key.</summary>
    Down,

    /// <summary>The Home key.</summary>
    Home,
    /// <summary>The End key.</summary>
    End,
    /// <summary>The Page Up key.</summary>
    PageUp,
    /// <summary>The Page Down key.</summary>
    PageDown,

    /// <summary>The number key 0.</summary>
    D0,
    /// <summary>The number key 1.</summary>
    D1,
    /// <summary>The number key 2.</summary>
    D2,
    /// <summary>The number key 3.</summary>
    D3,
    /// <summary>The number key 4.</summary>
    D4,
    /// <summary>The number key 5.</summary>
    D5,
    /// <summary>The number key 6.</summary>
    D6,
    /// <summary>The number key 7.</summary>
    D7,
    /// <summary>The number key 8.</summary>
    D8,
    /// <summary>The number key 9.</summary>
    D9
