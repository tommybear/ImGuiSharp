namespace ImGuiSharp.Input;

/// <summary>
/// Represents mouse buttons recognised by the immediate-mode input system.
/// </summary>
public enum ImGuiMouseButton
{
    /// <summary>
    /// The left mouse button.
    /// </summary>
    Left = 0,

    /// <summary>
    /// The right mouse button.
    /// </summary>
    Right = 1,

    /// <summary>
    /// The middle mouse button.
    /// </summary>
    Middle = 2
}
